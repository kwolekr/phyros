void RC4Crypt(unsigned char *key, unsigned char *data, int length) {
	int i;
	for (i = 0; i < length; i++) {
		key[0x100]++;
		key[0x101] += key[key[0x100]];
		SWAP(key[key[0x101]], key[key[0x100]]);
		data[i] = data[i] ^ key[(key[key[0x101]] + key[key[0x100]]) & 0xFF];
	}
}


int CryptFile(char *filename) {
	int len;
	unsigned char *buf;

	FILE *file = fopen(filename, "rb+");
	if (!file)
		return 0;

	fseek(file, 0, SEEK_END);
	len = ftell(file);
	rewind(file);
	
	buf = (unsigned char *)malloc(len);
	fread(buf, len, 1, file);
	rewind(file);

	RC4Crypt(updatekey, buf, len);

	fwrite(buf, len, 1, file);

	fclose(file);
	return 1;
}


